r"""Sweep the launch-configuration tables of the shape-tuned SeZM kernels.

Every table served by :mod:`.tile_configs` is generated by this module.  A
sweep measures the candidate launch configurations of one kernel family for
one shape key on synthetic tensors at production edge counts, applies the
family's win rule, and returns the resulting entries (a configuration
tuple, or ``None`` where the family default is the measured optimum).

The module is used in three ways:

1. *Freeze auto-tuning* -- :func:`tune_missing_configs` sweeps exactly the
   shape keys a model needs that are absent from both the built-in tables
   and the current process registrations, and registers the winners via
   :func:`~.tile_configs.register_tile_configs`.  The SeZM ``.pt2`` freeze
   path calls this on the local GPU before tracing, so the frozen artifact
   bakes launches tuned for the deployment hardware even when the GPU has
   no built-in coverage.  Keys whose sweep concluded "keep the default"
   are registered as ``None`` and are not re-swept on later freezes.
2. *Manual sweeps* -- the CLI sweeps one explicit shape key
   (``--cf/--lmax``) or every key a checkpoint needs (``--model``), and
   prints the resulting entries as a Python fragment in the layout of
   :mod:`.tile_config_data`, ready to be merged under a new GPU name when
   extending the built-in tables.
3. *Table regeneration* -- rerunning a sweep after any kernel-body change;
   see the mandatory-validation note below.

Usage
-----
::

    python -m deepmd.kernels.triton.sezm.sweep_tile_configs \\
        --cf CF --lmax LMAX [--kernels FAMILY[,FAMILY...]] [--focus F]
        [--heads H] [--edges E] [--device cuda:0]

    python -m deepmd.kernels.triton.sezm.sweep_tile_configs \\
        --model model.pt [--level 3] [--edges E] [--device cuda:0]

``--kernels`` selects the sweep groups (default: all):

``pointwise``
    The gate / recompute / backward-pointwise kernels, keyed
    ``(focus_dim, lmax)`` -> the ``gate`` / ``recompute`` / ``point``
    families.  For ``cf >= GATE_BMM_MIN_FOCUS_DIM`` the gate projection
    runs as a cuBLAS bmm (reported once for reference) and the recompute
    kernel is not swept because it is never launched in that regime.
``rotate_fwd``
    The rotate+mix forward kernel, keyed ``(C_wide, lmax)`` ->
    ``rotate_mix_fwd``; ``None`` records that the upstream default won.
``rotate_bwd``
    The edge-block rotate+mix backward against the per-edge kernel, keyed
    ``(C_wide, lmax)`` -> ``rotate_mix_bwd_block``.  The family is a win
    list: the entry is a configuration only when the speedup exceeds the
    3% noise margin, otherwise ``None`` keeps the per-edge kernel.
``flash_bwd``
    The edge-block flash-attention backward against the per-edge kernel,
    keyed ``(C_wide, lmax)`` -> ``flash_bwd_block``; the same win-list
    rule applies.
``fp16x3``
    The four fp16x3 mixing-stack GEMMs, keyed ``(focus_dim, lmax)`` ->
    ``stack_fp16x3``.  Every candidate is ranked by standalone kernel time
    and then validated end to end against an fp64 reference of the whole
    stack operator before it may win; ``None`` records that no candidate
    validated and the fp32 stack stays in charge.

Interpretation guidance
-----------------------
The winning ``BLOCK_M`` of the pointwise kernels and the winning ``BLOCK_E``
of the edge-block kernels shrink monotonically as the register-pressure
products ``lmax * next_power_of_2(cf)`` and ``lmax * next_power_of_2(C_wide)``
grow; a candidate on the wrong side of the spill point can be an order of
magnitude slower, which is why the tables are exact-keyed rather than
heuristic.  For the fp32 kernels, tile choices never affect numerical
results, so a sweep only ever changes speed.  Winning configurations are
insensitive to the edge count once the device is saturated (the defaults
sweep at 6.5e5 edges); small systems are launch-bound and insensitive to
the choice altogether.

fp16x3 validation is mandatory
------------------------------
Some ``(num_warps, num_stages)`` combinations of the fp16x3 three-``tl.dot``
k-loop are miscompiled by the Triton software pipeliner into silent NaN rows.
The affected set shifts both with any change to the kernel body *and with the
edge count*: a config finite at one count can produce NaN at another, because
the miscompilation follows the launch grid rather than the data.  The fp16x3
sweep therefore validates every candidate through the full operator (forward
and force backward) in two ways before timing may crown it: an fp64 accuracy
check at a fixed count, and a finiteness check across an intermediate spread
of edge counts (``_FP16X3_FINITE_EDGES``) plus the main sweep count.  A
candidate that is inaccurate or non-finite at any of these is skipped.  The
edge-count spread reduces but cannot eliminate the risk (no finite sample
certifies every count), which is why ``num_stages == 1`` -- the
pipeliner-free family that is structurally NaN-free at any count -- stays in
the candidate grid.  Writing a hand-picked configuration into the tables
without this validation risks silently corrupted inference.  Sweeps at
``lmax >= 5`` compile deeply unrolled kernels and can take several minutes per
candidate; the reduced candidate grid keeps this tractable.
"""

