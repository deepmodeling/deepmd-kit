def collect_model_shape_keys(model: torch.nn.Module) -> list[tuple[int, int, int, int]]:
    """Collect the shape keys of every fused-value-path convolution in ``model``.

    Parameters
    ----------
    model : torch.nn.Module
        A constructed SeZM model (weights need not be loaded; the shapes are
        fixed by the hyperparameters).

    Returns
    -------
    list[tuple[int, int, int, int]]
        Deduplicated ``(focus_dim, lmax, n_focus, n_head)`` tuples, one per
        distinct convolution shape whose layout is supported by the fused
        value path.  Convolutions outside the supported layout never query
        the tables and contribute no keys.
    """
    from deepmd.pt.model.descriptor.sezm_nn.so2 import (
        SO2Convolution,
    )

    from .so2_value_path import (
        _is_supported,
    )

    keys: list[tuple[int, int, int, int]] = []
    for module in model.modules():
        if not isinstance(module, SO2Convolution) or not _is_supported(module):
            continue
        n_head = module.n_atten_head if module.n_atten_head > 0 else 1
        key = (module.so2_focus_dim, module.lmax, module.n_focus, n_head)
        if key not in keys:
            keys.append(key)
    return keys


def tune_missing_configs(
    shape_keys: list[tuple[int, int, int, int]],
    *,
    level: int,
    device: torch.device | str = "cuda",
    n_edge: int | None = None,
) -> SweepResult:
    """Sweep and register every table key the given shapes need but lack.

    For each ``(focus_dim, lmax, n_focus, n_head)`` shape, every sweep group
    relevant at the given ``DP_TRITON_INFER`` level (the table-configured
    kernels at level 2, plus the fp16x3 stack at level 3) is checked against
    :func:`~.tile_configs.has_tile_config` and run only when its sentinel
    key is absent from both the built-in tables and the current process
    registrations.  Results -- including "the default won" conclusions --
    are registered for the current process, so a subsequent trace bakes
    them and a subsequent call skips them.

    Parameters
    ----------
    shape_keys : list[tuple[int, int, int, int]]
        Shapes as returned by :func:`collect_model_shape_keys`.
    level : int
        The active Triton inference level; levels below 2 tune nothing.
    device : torch.device or str
        CUDA device to sweep on.
    n_edge : int or None
        Edge count of the synthetic sweep tensors; ``None`` selects a
        width-scaled saturating count per kernel family.

    Returns
    -------
    SweepResult
        All newly registered entries, empty when every key was covered.
    """
    registered: SweepResult = {}
    if level < 2:
        return registered
    for cf, lmax, n_focus, n_head in shape_keys:
        c_wide = n_focus * cf
        pending: list[tuple[str, dict[str, Any]]] = []
        for group, sentinel in _GROUP_SENTINELS.items():
            if group == "fp16x3" and level < 3:
                continue
            key = (cf, lmax) if group in ("pointwise", "fp16x3") else (c_wide, lmax)
            if has_tile_config(sentinel, key):
                continue
            kwargs: dict[str, Any] = {
                "n_focus": n_focus,
                "n_edge": n_edge,
                "device": device,
            }
            if group == "flash_bwd":
                kwargs["n_head"] = n_head
            pending.append((group, kwargs))
        if not pending:
            continue
        log.info(
            "Tuning SeZM Triton launch configurations for shape "
            "(focus_dim=%d, lmax=%d, n_focus=%d) on %s: groups %s. "
            "This runs once per uncovered shape; expect minutes on narrow "
            "widths and up to tens of minutes on wide channels at high lmax "
            "(kernel compilation dominates).",
            cf,
            lmax,
            n_focus,
            torch.cuda.get_device_name(torch.device(device)),
            [group for group, _ in pending],
        )
        for group, kwargs in pending:
            result = _SWEEPS[group](cf, lmax, **kwargs)
            for family, entries in result.items():
                register_tile_configs(family, entries)
                registered.setdefault(family, {}).update(entries)
            # Synthetic sweep tensors approach a third of the device memory;
            # returning them to the driver between groups keeps consecutive
            # sweeps (and the model being frozen) from fragmenting into OOM.
            torch.cuda.empty_cache()
    return registered


def _format_data_fragment(entries: SweepResult) -> str:
    """Render registered entries as a :mod:`.tile_config_data` fragment."""
    if not torch.cuda.is_available():
        return ""
    device_name = torch.cuda.get_device_name(torch.cuda.current_device())
    lines = [f'    "{device_name}": {{']
    for family in sorted(entries):
        lines.append(f'        "{family}": {{')
        for key in sorted(entries[family]):
            lines.append(f"            {key}: {entries[family][key]},")
        lines.append("        },")
    lines.append("    },")
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Sweep launch configurations of the shape-tuned SeZM "
        "Triton kernels, either for one explicit (cf, lmax) key or for "
        "every key a checkpoint needs."
    )
    parser.add_argument("--cf", type=int, help="per-focus width Cf")
    parser.add_argument("--lmax", type=int, help="maximum degree")
    parser.add_argument("--model", help="checkpoint path; sweeps its missing keys")
    parser.add_argument(
        "--level",
        type=int,
        default=3,
        help="tune for this DP_TRITON_INFER level (with --model)",
    )
    parser.add_argument("--focus", type=int, default=2, help="focus count F")
    parser.add_argument("--heads", type=int, default=1, help="attention head count")
    parser.add_argument(
        "--edges",
        type=int,
        default=None,
        help="edge count E (default: width-scaled saturating count)",
    )
    parser.add_argument("--device", default="cuda", help="CUDA device string")
    parser.add_argument(
        "--kernels",
        default=",".join(_SWEEPS),
        help=f"comma list of sweep groups (with --cf/--lmax), from {sorted(_SWEEPS)}",
    )
    args = parser.parse_args()

    torch.manual_seed(0)
    torch.backends.cuda.matmul.allow_tf32 = False
    device = torch.device(args.device)
    if device.type == "cuda" and device.index is not None:
        torch.cuda.set_device(device)

    if args.model is not None:
        from deepmd.pt.entrypoints.freeze_pt2 import (
            _extract_state_and_params,
        )
        from deepmd.pt.model.model import (
            get_model,
        )

        raw = torch.load(args.model, map_location="cpu", weights_only=False)
        _, params = _extract_state_and_params(raw)
        branch_params = (
            list(params["model_dict"].values()) if "model_dict" in params else [params]
        )
        shape_keys = []
        for branch in branch_params:
            for key in collect_model_shape_keys(get_model(branch)):
                if key not in shape_keys:
                    shape_keys.append(key)
        print(f"model shape keys (cf, lmax, F, H): {shape_keys}")
        registered = tune_missing_configs(
            shape_keys, level=args.level, device=device, n_edge=args.edges
        )
    else:
        if args.cf is None or args.lmax is None:
            parser.error("either --model or both --cf and --lmax are required")
        groups = [name.strip() for name in args.kernels.split(",") if name.strip()]
        unknown = sorted(set(groups) - set(_SWEEPS))
        if unknown:
            parser.error(f"unknown sweep groups: {unknown}")
        print(
            f"cf={args.cf} lmax={args.lmax} focus={args.focus} heads={args.heads} "
            f"edges={args.edges}"
        )
        registered = {}
        for name in groups:
            print(f"== {name} ==")
            kwargs: dict[str, Any] = {
                "n_focus": args.focus,
                "n_edge": args.edges,
                "device": device,
            }
            if name == "flash_bwd":
                kwargs["n_head"] = args.heads
            result = _SWEEPS[name](args.cf, args.lmax, **kwargs)
            for family, entries in result.items():
                register_tile_configs(family, entries)
                registered.setdefault(family, {}).update(entries)

    if registered:
        print("\n# tile_config_data.py fragment (merge under the GPU name):")
        print(_format_data_fragment(registered))
    else:
        print("all requested keys are already covered; nothing swept")


if __name__ == "__main__":
    main()
