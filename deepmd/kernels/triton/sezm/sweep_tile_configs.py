# Pointwise group (gate / recompute / backward pointwise)
# ======================================================================
def sweep_pointwise(
    cf: int,
    lmax: int,
    *,
    n_focus: int = 2,
    n_edge: int | None = None,
    device: torch.device | str = "cuda",
) -> SweepResult:
    """Sweep the gate / recompute / backward-pointwise launch triples.

    Parameters
    ----------
    cf : int
        Per-focus channel width ``Cf``.
    lmax : int
        Maximum spherical harmonic degree.
    n_focus : int
        Focus count of the synthetic tensors; the winners are valid for any
        focus count (the focus stream rides the grid batch axis).
    n_edge : int
        Edge count of the synthetic tensors.
    device : torch.device or str
        CUDA device to sweep on.

    Returns
    -------
    SweepResult
        Entries for the ``gate``, ``point`` and (below the bmm regime)
        ``recompute`` families under the key ``(cf, lmax)``.
    """
    device = torch.device(device)
    if n_edge is None:
        n_edge = _saturating_edges(cf)
    row = (3 * lmax + 1) * cf
    gate_width = lmax * cf
    use_bmm = cf >= GATE_BMM_MIN_FOCUS_DIM

    u = torch.randn(n_focus, n_edge, row, device=device)
    z_all = torch.randn(1, n_focus, n_edge, row, device=device)
    gw_all = torch.randn(1, n_focus, cf, gate_width, device=device) * 0.05
    gwt_all = gw_all.transpose(2, 3).contiguous()
    v = torch.empty_like(u)
    sig = torch.rand(n_focus, n_edge, gate_width, device=device, dtype=torch.float32)
    grad = torch.randn(n_focus, n_edge, row, device=device)
    gz = torch.empty_like(grad)
    glogit = torch.empty_like(sig) if use_bmm else sig

    kernels = {
        "gate": lambda bm, w, s: _stack_gate_kernel[(triton.cdiv(n_edge, bm), n_focus)](
            u,
            z_all,
            gw_all,
            v,
            sig,
            n_edge,
            0,
            L=lmax,
            CF=cf,
            SIG_IN=use_bmm,
            BLOCK_M=bm,
            num_warps=w,
            num_stages=s,
        ),
        "recompute": lambda bm, w, s: _stack_recompute_kernel[
            (triton.cdiv(n_edge, bm), n_focus)
        ](
            z_all,
            gw_all,
            sig,
            n_edge,
            0,
            L=lmax,
            CF=cf,
            BLOCK_M=bm,
            num_warps=w,
            num_stages=s,
        ),
        "point": lambda bm, w, s: _stack_point_bwd_kernel[
            (triton.cdiv(n_edge, bm), n_focus)
        ](
            grad,
            z_all,
            sig,
            gwt_all,
            gz,
            glogit,
            n_edge,
            0,
            L=lmax,
            CF=cf,
            GLOGIT_OUT=use_bmm,
            BLOCK_M=bm,
            num_warps=w,
            num_stages=s,
        ),
    }
    if use_bmm:
        projection_ms = _bench(
            lambda: torch.sigmoid(torch.bmm(z_all[0, :, :, :cf], gw_all[0]), out=sig)
        )
        print(f"[bmm sigmoid projection: {projection_ms:.3f} ms]")
        kernels.pop("recompute")

    result: SweepResult = {}
    for family, launch in kernels.items():
        best_ms, best_cfg = float("inf"), None
        for bm, w, s in itertools.product(
            _BLOCK_M_CANDIDATES, _WARP_CANDIDATES, _STAGE_CANDIDATES
        ):
            try:
                ms = _bench(lambda: launch(bm, w, s))
            except triton.runtime.errors.OutOfResources:
                print(f"  BM={bm:3d} warps={w:2d} stages={s}: out of resources")
                continue
            marker = ""
            if ms < best_ms:
                best_ms, best_cfg = ms, (bm, w, s)
                marker = "  <-"
            print(f"  BM={bm:3d} warps={w:2d} stages={s}: {ms:8.3f} ms{marker}")
        print(f"BEST {family}[({cf}, {lmax})] = {best_cfg}  # {best_ms:.3f} ms")
        result[family] = {(cf, lmax): best_cfg}
    return result


# ======================================================================
# Rotate+mix forward
# ======================================================================
def sweep_rotate_fwd(
    cf: int,
    lmax: int,
    *,
    n_focus: int = 2,
    n_edge: int | None = None,
    device: torch.device | str = "cuda",
) -> SweepResult:
    """Sweep the rotate+mix forward ``(num_warps, num_stages)`` pair.

    Returns the ``rotate_mix_fwd`` entry under the key
    ``(n_focus * cf, lmax)``; ``None`` records that the upstream default
    ``(2, 2)`` is the measured optimum.
    """
    device = torch.device(device)
    c_wide = n_focus * cf
    if n_edge is None:
        n_edge = _saturating_edges(c_wide)
    row = (3 * lmax + 1) * cf
    kernel_size = (lmax + 1) ** 2 + lmax**2
    n_nodes = max(n_edge // 128, 1)

    x = torch.randn(n_nodes, (lmax + 1) ** 2, c_wide, device=device)
    src = torch.randint(0, n_nodes, (n_edge,), device=device)
    wigner = _block_diag_wigner(n_edge, lmax, device)
    kc = torch.randn(n_edge, kernel_size, device=device)
    cb = torch.randn(1, c_wide, device=device)
    u = torch.empty(n_focus, n_edge, row, device=device, dtype=torch.float32)

    def launch(warps: int, stages: int) -> None:
        _rotate_mix_fwd_kernel[(n_edge,)](
            x,
            src,
            wigner,
            kc,
            cb,
            u,
            n_edge,
            x.stride(0),
            x.stride(1),
            L=lmax,
            CF=cf,
            CW=c_wide,
            BC=triton.next_power_of_2(c_wide),
            RANK=1,
            num_warps=warps,
            num_stages=stages,
        )

    best_ms, best_cfg = float("inf"), None
    for warps, stages in _ROTATE_FWD_CANDIDATES:
        try:
            ms = _bench(lambda: launch(warps, stages))
        except triton.runtime.errors.OutOfResources:
            continue
        marker = ""
        if ms < best_ms:
            best_ms, best_cfg = ms, (warps, stages)
            marker = "  <-"
        print(f"  warps={warps:2d} stages={stages}: {ms:8.3f} ms{marker}")
    entry = None if best_cfg == _ROTATE_FWD_DEFAULT else best_cfg
    print(
        f"BEST rotate_mix_fwd[({c_wide}, {lmax})] = {entry}  # {best_ms:.3f} ms"
        + ("  (upstream default)" if entry is None else "")
    )
    return {"rotate_mix_fwd": {(c_wide, lmax): entry}}


# ======================================================================
# Edge-block backward groups (win lists against the per-edge kernels)
# ======================================================================
def _win_list_entry(
    family: str, key: tuple[int, int], best: tuple[float, tuple] | None, base_ms: float
) -> tuple | None:
    """Apply the win rule: record the entry only above the 3% margin."""
    if best is None:
        print(f"BEST {family}[{key}]: no valid candidate; keep the per-edge kernel")
        return None
    speedup = base_ms / best[0]
    verdict = "RECORD" if speedup >= _WIN_MARGIN else "keep the per-edge kernel"
    print(
        f"BEST {family}[{key}] = {best[1]}  # {best[0]:.3f} ms vs per-edge "
        f"{base_ms:.3f} ms ({speedup:.2f}x) -> {verdict}"
    )
    return best[1] if speedup >= _WIN_MARGIN else None


def sweep_rotate_bwd(
    cf: int,
    lmax: int,
    *,
    n_focus: int = 2,
    n_edge: int | None = None,
    device: torch.device | str = "cuda",
) -> SweepResult:
    """Sweep the edge-block rotate+mix backward against the per-edge kernel.

    Returns the ``rotate_mix_bwd_block`` win-list entry under the key
    ``(n_focus * cf, lmax)``.
    """
    device = torch.device(device)
    c_wide = n_focus * cf
    if n_edge is None:
        n_edge = _saturating_edges(c_wide)
    dim = (lmax + 1) ** 2
    row = (3 * lmax + 1) * cf
    kernel_size = (lmax + 1) ** 2 + lmax**2
    n_nodes = max(n_edge // 128, 1)

    grad_u = torch.randn(n_focus, n_edge, row, device=device)
    x = torch.randn(n_nodes, dim, c_wide, device=device)
    src = torch.randint(0, n_nodes, (n_edge,), device=device)
    wigner = _block_diag_wigner(n_edge, lmax, device)
    kc = torch.randn(n_edge, kernel_size, device=device)
    cb = torch.randn(1, c_wide, device=device)

    reference = _rotate_mix_bwd_op(grad_u, x, src, wigner, kc, cb, lmax, n_focus, 1)
    base_ms = _bench(
        lambda: _rotate_mix_bwd_op(grad_u, x, src, wigner, kc, cb, lmax, n_focus, 1),
        iters=8,
    )

    def launch(block_e: int, warps: int, stages: int) -> tuple[torch.Tensor, ...]:
        gxe = torch.empty(n_edge, dim, c_wide, device=device, dtype=torch.float32)
        gw = torch.zeros_like(wigner)
        gkc = torch.empty_like(kc)
        wrap_triton(_rotate_mix_bwd_block_kernel)[(triton.cdiv(n_edge, block_e),)](
            grad_u,
            x,
            src,
            wigner,
            kc,
            cb,
            gxe,
            gw,
            gkc,
            n_edge,
            x.stride(0),
            x.stride(1),
            L=lmax,
            CF=cf,
            CW=c_wide,
            CP=triton.next_power_of_2(c_wide),
            RANK=1,
            BLOCK_E=block_e,
            num_warps=warps,
            num_stages=stages,
        )
        return gxe, gw, gkc

    best = None
    for cfg in _EDGE_BLOCK_CANDIDATES:
        try:
            outputs = launch(*cfg)
            torch.cuda.synchronize()
            err = max(_relerr(o, r) for o, r in zip(outputs, reference))
            if err > 5e-6:
                continue
            ms = _bench(lambda: launch(*cfg), iters=8)
        except triton.runtime.errors.OutOfResources:
            continue
        if best is None or ms < best[0]:
            best = (ms, cfg)
            print(f"  BE={cfg[0]:3d} warps={cfg[1]} stages={cfg[2]}: {ms:8.3f} ms  <-")
    key = (c_wide, lmax)
    return {
        "rotate_mix_bwd_block": {
            key: _win_list_entry("rotate_mix_bwd_block", key, best, base_ms)
        }
    }


def sweep_flash_bwd(
    cf: int,
    lmax: int,
    *,
    n_focus: int = 2,
    n_head: int = 1,
    n_edge: int | None = None,
    device: torch.device | str = "cuda",
) -> SweepResult:
    """Sweep the edge-block flash-attention backward against the per-edge kernel.

    Returns the ``flash_bwd_block`` win-list entry under the key
    ``(n_focus * cf, lmax)``.  ``n_head`` only specializes the kernel binary
    through a ``constexpr``; the winning schedule is shared across head
    counts of the same width.
    """
    device = torch.device(device)
    c_wide = n_focus * cf
    if n_edge is None:
        n_edge = _saturating_edges(c_wide)
    dim = (lmax + 1) ** 2
    reduced_dim = 3 * lmax + 1
    n_nodes = max(n_edge // 128, 1)

    grad_pre_gate = torch.randn(n_nodes, dim, c_wide, device=device)
    x_local = torch.randn(n_edge, n_focus, reduced_dim, cf, device=device)
    wigner_dt = _block_diag_wigner(n_edge, lmax, device)
    rescale = torch.rand(dim, device=device, dtype=torch.float32) + 0.5
    alpha = torch.rand(n_edge, n_focus, n_head, device=device, dtype=torch.float32)
    dst = torch.randint(0, n_nodes, (n_edge,), device=device)

    reference = _flash_bwd_op(
        grad_pre_gate, x_local, wigner_dt, rescale, alpha, dst, lmax, n_head
    )
    base_ms = _bench(
        lambda: _flash_bwd_op(
            grad_pre_gate, x_local, wigner_dt, rescale, alpha, dst, lmax, n_head
        ),
        iters=8,
    )

    def launch(block_e: int, warps: int, stages: int) -> tuple[torch.Tensor, ...]:
        gxl = torch.empty_like(x_local)
        gdt = torch.zeros_like(wigner_dt)
        gw = torch.empty_like(alpha)
        wrap_triton(_flash_bwd_block_kernel)[(triton.cdiv(n_edge, block_e),)](
            grad_pre_gate,
            x_local,
            wigner_dt,
            rescale,
            alpha,
            dst,
            gxl,
            gdt,
            gw,
            n_edge,
            grad_pre_gate.stride(0),
            grad_pre_gate.stride(1),
            x_local.stride(0),
            x_local.stride(1),
            x_local.stride(2),
            x_local.stride(3),
            gxl.stride(0),
            gxl.stride(1),
            gxl.stride(2),
            gxl.stride(3),
            L=lmax,
            CF=cf,
            CW=c_wide,
            CP=triton.next_power_of_2(c_wide),
            HEAD_DIM=cf // n_head,
            NHEAD=n_head,
            BLOCK_E=block_e,
            num_warps=warps,
            num_stages=stages,
        )
        return gxl, gdt, gw

    best = None
    for cfg in _EDGE_BLOCK_CANDIDATES:
        try:
            outputs = launch(*cfg)
            torch.cuda.synchronize()
            err = max(_relerr(o, r) for o, r in zip(outputs, reference))
            if err > 5e-6:
                continue
            ms = _bench(lambda: launch(*cfg), iters=8)
        except triton.runtime.errors.OutOfResources:
            continue
        if best is None or ms < best[0]:
            best = (ms, cfg)
            print(f"  BE={cfg[0]:3d} warps={cfg[1]} stages={cfg[2]}: {ms:8.3f} ms  <-")
    key = (c_wide, lmax)
    return {
        "flash_bwd_block": {key: _win_list_entry("flash_bwd_block", key, best, base_ms)}
    }


# ======================================================================
# fp16x3 mixing-stack GEMMs (fp64-validated)
# ======================================================================
def sweep_fp16x3(
    cf: int,
    lmax: int,
    *,
    n_focus: int = 2,
    n_edge: int | None = None,
    device: torch.device | str = "cuda",
) -> SweepResult:
    """Sweep and validate the four fp16x3 stack GEMM configurations.

    Returns the ``stack_fp16x3`` entry under the key ``(cf, lmax)``:
    the fastest fp64-validated combination, or ``None`` when no candidate
    validates (the fp32 stack stays in charge).  Validation runs through
    the production operator, so trial configurations are registered into
    the runtime table during the sweep; the final conclusion is registered
    unconditionally on exit.
    """
    device = torch.device(device)
    if n_edge is None:
        n_edge = _saturating_edges(cf)
    n_layers = 3
    m0 = (lmax + 1) * cf
    m1 = 2 * lmax * cf
    half = lmax * cf
    row = (3 * lmax + 1) * cf

    u0 = torch.randn(n_focus, n_edge, row, device=device)
    alpha = torch.rand(n_edge, n_focus, device=device, dtype=torch.float32) + 0.1
    w0_all = torch.randn(n_layers, n_focus, m0, m0, device=device) * 0.2
    block_u = torch.randn(n_layers, n_focus, half, half, device=device) * 0.2
    block_v = torch.randn(n_layers, n_focus, half, half, device=device) * 0.2
    # The |m| = 1 weight carries the [[U, V], [-V, U]] complex structure of
    # SO2Linear so the synthetic stack matches the production operator.
    w1_all = torch.cat(
        [
            torch.cat([block_u, block_v], dim=3),
            torch.cat([-block_v, block_u], dim=3),
        ],
        dim=2,
    ).contiguous()
    gw_all = torch.randn(n_layers - 1, n_focus, cf, half, device=device) * 0.3
    w0h, w0l = _split_fp16(w0_all)
    w1h, w1l = _split_fp16(w1_all)
    w0t = w0_all.transpose(2, 3).contiguous()
    w1t = w1_all.transpose(2, 3).contiguous()
    w0th, w0tl = _split_fp16(w0t)
    w1th, w1tl = _split_fp16(w1t)
    z_all = torch.empty(
        n_layers - 1, n_focus, n_edge, row, device=device, dtype=torch.float32
    )
    out = torch.empty_like(u0)
    sig = torch.full((n_focus, n_edge, half), 0.5, device=device, dtype=torch.float32)
    gz = torch.randn(n_focus, n_edge, row, device=device)
    g_res = torch.randn(n_focus, n_edge, row, device=device)
    gu = torch.empty_like(u0)

    def launch_m0(bm: int, bn: int, bk: int, warps: int, stages: int) -> None:
        wrap_triton(_stack_fp16x3_m0_kernel)[
            (triton.cdiv(n_edge, bm) * triton.cdiv(m0, bn), n_focus)
        ](
            u0,
            w0h,
            w0l,
            alpha,
            z_all,
            n_edge,
            0,
            L=lmax,
            CF=cf,
            EPILOGUE=0,
            V_EDGE_MAJOR=False,
            APPLY_ALPHA=False,
            BLOCK_M=bm,
            BLOCK_N=bn,
            BLOCK_K=bk,
            num_warps=warps,
            num_stages=stages,
        )

    def launch_m1(bm: int, bn: int, bk: int, warps: int, stages: int) -> None:
        wrap_triton(_stack_fp16x3_m1_kernel)[
            (triton.cdiv(n_edge, bm) * triton.cdiv(m1, bn), n_focus)
        ](
            u0,
            w1h,
            w1l,
            sig,
            alpha,
            out,
            z_all,
            n_edge,
            0,
            L=lmax,
            CF=cf,
            HAS_GATE=True,
            V_EDGE_MAJOR=False,
            APPLY_ALPHA=False,
            SAVE_Z=True,
            BLOCK_M=bm,
            BLOCK_N=bn,
            BLOCK_K=bk,
            num_warps=warps,
            num_stages=stages,
        )

    def launch_bwd(
        is_m1: bool, bm: int, bn: int, bk: int, warps: int, stages: int
    ) -> None:
        width = m1 if is_m1 else m0
        wh, wl = (w1th, w1tl) if is_m1 else (w0th, w0tl)
        wrap_triton(_stack_fp16x3_bwd_kernel)[
            (triton.cdiv(n_edge, bm) * triton.cdiv(width, bn), n_focus)
        ](
            gz,
            g_res,
            wh,
            wl,
            alpha,
            gu,
            n_edge,
            0,
            L=lmax,
            CF=cf,
            IS_M1=is_m1,
            G_EDGE_MAJOR=False,
            FOLD_ALPHA=False,
            RES_IS_GZ=False,
            BLOCK_M=bm,
            BLOCK_N=bn,
            BLOCK_K=bk,
            num_warps=warps,
            num_stages=stages,
        )

    launchers = {
        0: launch_m0,
        1: launch_m1,
        2: lambda *cfg: launch_bwd(False, *cfg),
        3: lambda *cfg: launch_bwd(True, *cfg),
    }
    names = ("forward m0", "forward |m|=1", "backward m0", "backward |m|=1")

    # === Step 1. Rank every candidate per kernel by standalone time ===
    ranked: dict[int, list[tuple[float, tuple]]] = {}
    for slot, launch in launchers.items():
        results = []
        for cfg in _FP16X3_GEMM_CANDIDATES:
            try:
                ms = _bench(lambda: launch(*cfg), iters=10)
            except triton.runtime.errors.OutOfResources:
                continue
            results.append((ms, cfg))
        results.sort()
        ranked[slot] = results

    # === Step 2. fp64 whole-op reference and validation harness ===
    n_check = min(_FP16X3_CHECK_EDGES, n_edge)
    truth_x, _ = _mixing_stack_reference(
        u0[:, :n_check].double(),
        alpha[:n_check].double(),
        w0_all.double(),
        w1_all.double(),
        gw_all.double(),
        lmax,
        cf,
        True,
    )
    truth_x = truth_x.float()
    u0_ref = u0[:, :n_check].double().requires_grad_(True)
    alpha_ref = alpha[:n_check].double().requires_grad_(True)
    x_ref, _ = _mixing_stack_reference(
        u0_ref,
        alpha_ref,
        w0_all.double(),
        w1_all.double(),
        gw_all.double(),
        lmax,
        cf,
        True,
    )
    grad_seed = torch.randn(n_check, n_focus, row, device=device)
    gu_ref, _ = torch.autograd.grad(x_ref, [u0_ref, alpha_ref], grad_seed.double())
    gu_ref = gu_ref.float()
    grad_pad = torch.zeros(n_edge, n_focus, row, device=device, dtype=torch.float32)
    grad_pad[:n_check] = grad_seed

    # Pre-built inputs for the cross-edge-count finiteness check (see
    # ``_FP16X3_FINITE_EDGES``).  Weights are edge-count independent, so only
    # the activation and the backward seed are rebuilt per count; counts at or
    # above the main sweep count are covered by ``whole_op_errors`` and skipped.
    finite_edges = [m for m in _FP16X3_FINITE_EDGES if m < n_edge]
    finite_inputs = [
        (
            torch.randn(n_focus, m, row, device=device),
            torch.rand(m, n_focus, device=device, dtype=torch.float32) + 0.1,
            torch.randn(m, n_focus, row, device=device),
        )
        for m in finite_edges
    ]

    def whole_op_errors() -> tuple[float, float, bool]:
        u0_run = u0.clone().requires_grad_(True)
        alpha_run = alpha.clone().requires_grad_(True)
        x_run, z_run = _mixing_stack_fp16x3_op(
            u0_run, alpha_run, w0_all, w1_all, gw_all, lmax, cf, True
        )
        finite = bool(torch.isfinite(x_run).all()) and bool(torch.isfinite(z_run).all())
        gu_run, _ = torch.autograd.grad(x_run, [u0_run, alpha_run], grad_pad)
        finite = finite and bool(torch.isfinite(gu_run).all())
        return (
            _relerr(x_run[:n_check], truth_x),
            _relerr(gu_run[:, :n_check], gu_ref),
            finite,
        )

    def finite_across_edges() -> bool:
        """Forward + backward finiteness of the installed config at the spread."""
        for u0_m, alpha_m, grad_m in finite_inputs:
            u0_r = u0_m.clone().requires_grad_(True)
            alpha_r = alpha_m.clone().requires_grad_(True)
            x_m, z_m = _mixing_stack_fp16x3_op(
                u0_r, alpha_r, w0_all, w1_all, gw_all, lmax, cf, True
            )
            if not (
                bool(torch.isfinite(x_m).all()) and bool(torch.isfinite(z_m).all())
            ):
                return False
            gu_m, _ = torch.autograd.grad(x_m, [u0_r, alpha_r], grad_m)
            if not bool(torch.isfinite(gu_m).all()):
                return False
        return True

    u0_fp32 = u0.clone().requires_grad_(True)
    alpha_fp32 = alpha.clone().requires_grad_(True)
    x_fp32, _ = _mixing_stack_op(
        u0_fp32, alpha_fp32, w0_all, w1_all, gw_all, lmax, cf, True
    )
    fp32_fwd_err = _relerr(x_fp32[:n_check], truth_x)
    gu_fp32, _ = torch.autograd.grad(x_fp32, [u0_fp32, alpha_fp32], grad_pad)
    fp32_bwd_err = _relerr(gu_fp32[:, :n_check], gu_ref)
    tol_fwd = max(_FP16X3_TOL_FACTOR * fp32_fwd_err, 2e-6)
    tol_bwd = max(_FP16X3_TOL_FACTOR * fp32_bwd_err, 8e-6)
    print(
        f"[fp32 reference errors: fwd {fp32_fwd_err:.2e}, bwd {fp32_bwd_err:.2e}; "
        f"tolerances fwd {tol_fwd:.2e}, bwd {tol_bwd:.2e}]"
    )

    def validate() -> bool:
        err_fwd, err_bwd, finite = whole_op_errors()
        if not (finite and err_fwd < tol_fwd and err_bwd < tol_bwd):
            return False
        # Accuracy and main-count finiteness hold; require finiteness across
        # the intermediate edge-count spread before accepting the config.
        return finite_across_edges()

    key = (cf, lmax)

    def install(entry: tuple | None) -> None:
        register_tile_configs("stack_fp16x3", {key: entry})

    def conclude() -> tuple | None:
        """Validate candidates through the live table; return the winner."""
        # === Step 3. Find a pin combination that validates as a whole ===
        pin = None
        for candidate in _FP16X3_PIN_CANDIDATES:
            install((candidate,) * 4)
            if validate():
                pin = candidate
                break
        if pin is None:
            print(
                f"BEST stack_fp16x3[{key}]: no pin combination validates; "
                "the fp32 stack stays in charge"
            )
            return None
        print(f"[pin combination: {pin}]")

        # === Step 4. Per kernel, walk the speed ranking to the first
        # candidate that validates with the other three pinned ===
        chosen: list[tuple] = []
        for slot in range(4):
            picked = None
            for rank, (ms, cfg) in enumerate(ranked[slot]):
                entry = [pin] * 4
                entry[slot] = cfg
                install(tuple(entry))
                if validate():
                    picked = (ms, cfg, rank)
                    break
            if picked is None:
                print(
                    f"BEST stack_fp16x3[{key}]: {names[slot]} has no "
                    "validating candidate; the fp32 stack stays in charge"
                )
                return None
            chosen.append(picked)
            print(
                f"  {names[slot]}: rank #{picked[2]} {picked[1]} @ {picked[0]:.3f} ms"
            )

        # === Step 5. Joint validation; fall back to the pin on failure ===
        install(tuple(cfg for _ms, cfg, _rank in chosen))
        if not validate():
            print("[joint validation failed; falling back to the pin combination]")
            install((pin,) * 4)
            if not validate():
                print(
                    f"BEST stack_fp16x3[{key}]: pin combination regressed; "
                    "the fp32 stack stays in charge"
                )
                return None
            chosen = [(float("nan"), pin, -1)] * 4
        conclusion = tuple(cfg for _ms, cfg, _rank in chosen)
        print(f"BEST stack_fp16x3[{key}] = {conclusion}")
        return conclusion

    # Trial installs must never outlive the sweep.  A completed run replaces
    # them with its conclusion (a validated combination, or None recording
    # that the fp32 stack won); an aborted run (OOM, compilation failure)
    # restores the pre-sweep entry so the key does not read as swept.
    runtime = _runtime_tile_configs("stack_fp16x3")
    had_prior = key in runtime
    prior = runtime.get(key)
    try:
        conclusion = conclude()
    except BaseException:
        if had_prior:
            install(prior)
        else:
            runtime.pop(key, None)
        raise
    install(conclusion)
    return {"stack_fp16x3": {key: conclusion}}


_SWEEPS: dict[str, Callable[..., SweepResult]] = {
    "pointwise": sweep_pointwise,
    "rotate_fwd": sweep_rotate_fwd,
    "rotate_bwd": sweep_rotate_bwd,
    "flash_bwd": sweep_flash_bwd,
    "fp16x3": sweep_fp16x3,
}

# Sentinel family per sweep group: the group has run for a key exactly when
# its sentinel family carries the key (``sweep_pointwise`` skips the
# recompute kernel in the bmm regime, so ``gate`` is the group sentinel).
_GROUP_SENTINELS = {
    "pointwise": "gate",
    "rotate_fwd": "rotate_mix_fwd",
    "rotate_bwd": "rotate_mix_bwd_block",
    "flash_bwd": "flash_bwd_block",
    "fp16x3": "stack_fp16x3",
}


