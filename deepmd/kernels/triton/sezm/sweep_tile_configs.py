# Edge counts at which every candidate's finiteness is re-checked (forward
# and backward), in addition to the accuracy check and the finiteness at the
# main sweep count.  The Triton software pipeliner can miscompile the
# three-``tl.dot`` k-loop into silent NaN at *some* edge counts while
# remaining finite at others -- the affected set shifts with the launch grid,
# so a single-edge-count check does not certify a config for the arbitrary
# edge counts an MD run presents.  This intermediate spread (below the main
# count, which is checked separately) samples the band where the
# miscompilation was observed; it reduces rather than eliminates the risk, so
# ``num_stages == 1`` configs (pipeliner off, structurally NaN-free at any
# edge count) remain in the candidate grid as the safe fallback.
_FP16X3_FINITE_EDGES = (4096, 20000, 65537, 131072)

# Win margin of the edge-block families against the per-edge kernels; a
# speedup below this noise floor keeps the per-edge kernel.
_WIN_MARGIN = 1.03

_DEFAULT_EDGES = 650000


def _saturating_edges(width: int) -> int:
    """Return a device-saturating edge count for the given channel width.

    Winning configurations are insensitive to the edge count once the device
    is saturated (measured winners are stable down to ~2e5 edges and start
    to drift below ~5e4), and the per-edge tensor footprint grows linearly
    with the channel width, so the sweep scales the synthetic edge count
    inversely with the width to keep peak memory roughly constant.  The
    reference point is 6.5e5 edges at width 64 -- the production scale the
    built-in tables were swept at.

    The count is additionally capped so the sweep's peak allocation stays
    within roughly a third of the device's total memory, keeping the freeze
    auto-tuner viable on small-memory GPUs and beside a resident model.
    The per-edge cost model (~160 KB per edge at width 64, scaling with the
    width) is the measured peak of the heaviest group -- the fp16x3 sweep,
    whose ranking buffers, fp64 validation reference and autograd graphs
    dwarf the synthetic tensors themselves.

    The memory budget always wins over tuning quality: measured winners are
    stable down to ~2e5 edges and drift only into neighboring near-optimal
    candidates below that (a few percent of kernel time, never correctness
    -- fp16x3 validity comes from the fp64 check, which is edge-count
    independent), so a reduced count merely costs a sliver of speed.  A
    sweep below 1e5 edges logs a notice to that effect; the hard floor of
    2e4 edges only keeps the timing signal above launch overhead.
    """
    edges = _DEFAULT_EDGES * 64 // max(width, 64)
    if torch.cuda.is_available():
        total = torch.cuda.get_device_properties(
            torch.cuda.current_device()
        ).total_memory
        bytes_per_edge = 160000 * max(width, 64) // 64
        cap = max(int(total / 3) // bytes_per_edge, 20000)
        if cap < min(edges, 100000):
            log.info(
                "Sweep edge count capped at %d by the device memory budget "
                "(device total %.1f GB); winning configurations may be a few "
                "percent off the saturated optimum.",
                cap,
                total / 2**30,
            )
        edges = min(edges, cap)
    return edges


# Entries returned by one sweep group: family name -> {shape key: entry}.
SweepResult = dict[str, dict[tuple[int, int], "tuple | None"]]


def _bench(fn: Callable[[], object], iters: int = 20) -> float:
    """Return the mean kernel time of ``fn`` in milliseconds."""
    for _ in range(3):
        fn()
    torch.cuda.synchronize()
    start = torch.cuda.Event(enable_timing=True)
    stop = torch.cuda.Event(enable_timing=True)
    start.record()
    for _ in range(iters):
        fn()
    stop.record()
    torch.cuda.synchronize()
    return start.elapsed_time(stop) / iters


def _relerr(a: torch.Tensor, b: torch.Tensor) -> float:
    """Return the max relative error of ``a`` against reference ``b``."""
    return float((a - b).abs().max() / b.abs().max().clamp_min(1e-30))


def _block_diag_wigner(n_edge: int, lmax: int, device: torch.device) -> torch.Tensor:
    """Return a random block-diagonal Wigner-D stand-in with shape (E, D, D)."""
    dim = (lmax + 1) ** 2
    wigner = torch.zeros(n_edge, dim, dim, device=device, dtype=torch.float32)
    for degree in range(lmax + 1):
        base, size = degree * degree, 2 * degree + 1
        wigner[:, base : base + size, base : base + size] = torch.randn(
            n_edge, size, size, device=device
        )
    return wigner


