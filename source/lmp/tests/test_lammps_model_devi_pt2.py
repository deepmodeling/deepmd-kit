"""Test LAMMPS model deviation with .pt2 (AOTInductor) models.

Uses model_devi_md0.pt2 and model_devi_md1.pt2 (two SE(A) models with different
seeds, fparam+aparam+default_fparam).  Expected values are computed dynamically
from Python DeepPot inference so they track model regeneration automatically.
"""

import os
from pathlib import (
    Path,
)

import constants
import numpy as np
import pytest
from lammps import (
    PyLammps,
)
from write_lmp_data import (
    write_lmp_data,
)

pt2_file0 = (
    Path(__file__).parent.parent.parent / "tests" / "infer" / "model_devi_md0.pt2"
)
pt2_file1 = (
    Path(__file__).parent.parent.parent / "tests" / "infer" / "model_devi_md1.pt2"
)
data_file = Path(__file__).parent / "data_model_devi_pt2.lmp"
md_file = Path(__file__).parent / "md_pt2.out"

# Coordinates and types — same coords as gen_model_devi.py
# type_map=["O"], so all atoms are type 1 in LAMMPS (1-indexed)
box = np.array([0, 13, 0, 13, 0, 13, 0, 0, 0])
coord = np.array(
    [
        [12.83, 2.56, 2.18],
        [12.09, 2.87, 2.74],
        [0.25, 3.32, 1.68],
        [3.36, 3.00, 1.81],
        [3.51, 2.51, 2.60],
        [4.27, 3.22, 1.56],
    ]
)
type_all = np.array([1, 1, 1, 1, 1, 1])

# fparam/aparam for the model_devi models (numb_fparam=1, numb_aparam=1)
fparam_val = 0.25852028
aparam_val = 0.25852028

# Expected values are computed at module load from Python DeepPot inference.
expected_e = None
expected_f = None
expected_v = None
expected_f2 = None
expected_v2 = None


def _compute_expected():
    """Load both .pt2 models via DeepPot and compute reference values."""
    global expected_e, expected_f, expected_v, expected_f2, expected_v2

    from deepmd.infer import (
        DeepPot,
    )

    box_9 = np.array([13.0, 0.0, 0.0, 0.0, 13.0, 0.0, 0.0, 0.0, 13.0])
    atype = [0, 0, 0, 0, 0, 0]
    fp = np.array([fparam_val])
    ap = np.array([aparam_val] * 6)

    results = []
    for pt2_path in [pt2_file0, pt2_file1]:
        dp = DeepPot(str(pt2_path))
        e, f, v, ae, av = dp.eval(
            coord.reshape(1, -1),
            box_9,
            atype,
            fparam=fp,
            aparam=ap,
            atomic=True,
        )
        results.append((e[0, 0], f[0], av[0]))

    expected_e = results[0][0]
    expected_f = results[0][1]
    # DeepPot returns virial; LAMMPS centroid/stress/atom returns stress = -virial
    expected_v = -results[0][2]
    expected_f2 = results[1][1]
    expected_v2 = -results[1][2]


def setup_module() -> None:
    if not pt2_file0.exists() or not pt2_file1.exists():
        pytest.skip("model_devi_md0.pt2 / model_devi_md1.pt2 not found")
    _compute_expected()
    write_lmp_data(box, coord, type_all, data_file)


def teardown_module() -> None:
    if data_file.exists():
        os.remove(data_file)


def _lammps(data_file_path=data_file, units="metal") -> PyLammps:
    lammps = PyLammps()
    lammps.units(units)
    lammps.boundary("p p p")
    lammps.atom_style("atomic")
    lammps.neighbor("2.0 bin")
    lammps.neigh_modify("every 10 delay 0 check no")
    lammps.read_data(data_file_path.resolve())
    lammps.mass("1 16")
    if units == "metal":
        lammps.timestep(0.0005)
    elif units == "real":
        lammps.timestep(0.5)
    lammps.fix("1 all nve")
    return lammps


@pytest.fixture()
def lammps():
    lmp = _lammps()
    yield lmp
    lmp.close()


@pytest.fixture()
def lammps_real():
    lmp = _lammps(units="real")
    yield lmp
    lmp.close()


def test_pair_deepmd_model_devi(lammps) -> None:
    lammps.pair_style(
        f"deepmd {pt2_file0.resolve()} {pt2_file1.resolve()} "
        f"out_file {md_file.resolve()} out_freq 1 atomic "
        f"fparam {fparam_val} aparam {aparam_val}"
    )
    lammps.pair_coeff("* *")
    lammps.run(0)
    assert lammps.eval("pe") == pytest.approx(expected_e)
    for ii in range(6):
        assert lammps.atoms[ii].force == pytest.approx(
            expected_f[lammps.atoms[ii].id - 1]
        )
    # load model devi
    md = np.loadtxt(md_file.resolve())
    expected_md_f = np.linalg.norm(np.std([expected_f, expected_f2], axis=0), axis=1)
    assert md[7:] == pytest.approx(expected_md_f)
    assert md[4] == pytest.approx(np.max(expected_md_f))
    assert md[5] == pytest.approx(np.min(expected_md_f))
    assert md[6] == pytest.approx(np.mean(expected_md_f))
    expected_md_v = (
        np.std([np.sum(expected_v, axis=0), np.sum(expected_v2, axis=0)], axis=0) / 6
    )
    assert md[1] == pytest.approx(np.max(expected_md_v))
    assert md[2] == pytest.approx(np.min(expected_md_v))
    assert md[3] == pytest.approx(np.sqrt(np.mean(np.square(expected_md_v))))


