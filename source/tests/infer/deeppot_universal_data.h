inline double total_energy(const DeepPotRef& ref) {
  return std::accumulate(ref.atomic_energy.begin(), ref.atomic_energy.end(),
                         0.0);
}

inline std::vector<double> total_virial(const DeepPotRef& ref) {
  std::vector<double> virial(9, 0.0);
  for (size_t atom = 0; atom < ref.atomic_energy.size(); ++atom) {
    for (size_t dd = 0; dd < 9; ++dd) {
      virial[dd] += ref.atomic_virial[atom * 9 + dd];
    }
  }
  return virial;
}

